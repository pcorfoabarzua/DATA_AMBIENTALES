LONGITUD FLOAT ,
ESTACION_DEL_ANIO VARCHAR2(20) ,
PROFUNDIDAD NUMBER(10,0),
FECHA_INGRESO DATE 
);

CREATE TABLE AMBIENTALES_MACROFAUNA_DETALLE(
AMBIENTALES_MACROFAUNA_DETALLE_ID NUMBER(38,0) ,
